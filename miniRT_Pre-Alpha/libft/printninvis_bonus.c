#include "libft.h"

void	printninvis(char *str, int n)
{
	int	i;

	i = -1;
	while (++i < n)
	{
		if (str[i] < 32 || str[i] > 126)
			write(1, "{!}", 3);
		else
			write(1, &str[i], 1);
	}
}
