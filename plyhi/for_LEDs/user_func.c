#include "LED.h"

color_t	EvenOdd(u16_t num)
{
	color_t	color;

	if (num % 2)
	{
		color.r = 0;
		color.g = 204;
		color.b = 252;
	}
	else
	{
		color.r = 255;
		color.g = 0;
		color.b = 102;
	}

	return color;
}

